function filterSwears(message) {
    const words = message.split(/\s+/);

    const filteredWords = words.map((word) => {
        if (swears.includes(word.toLowerCase())) {
            const length = word.length;
            const asterisks = "*".repeat(length - 2);
            return `${word.charAt(0)}${asterisks}${word.charAt(length - 1)}`;
        } else {
            return word;
        }
    });

    const filteredMessage = filteredWords.join(" ");

    return filteredMessage;
}


